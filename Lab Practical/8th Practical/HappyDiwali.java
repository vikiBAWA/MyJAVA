package diwali;

public class HappyDiwali {

	public static void main(String[] args) {
		//declaring the value string 
		String a = "WISHING YOU A HAPPY DIWALI";
		
		//using for loop for string
		for (int i=0;i<a.length();)
			
		{	
			System.out.println(a);
			//break statement to exit the loop after printing the string
		break;
		}
	}

}